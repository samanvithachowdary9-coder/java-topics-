// Indexed Character Printing in an Array
class Main {
    public static void main(String[] args) {
        int [] x = {1,2,3,4,5};
        System.out.println(x[4]);
    }
}
//Ordered Character printing in an Array
class Main {
    public static void main(String[] args) {
        int [] x = {1,2,3,4,5};
        for(int i = 0; i< x.length;i++) {
        System.out.println(x[i]);
        }
    }
}
//Sum of Array
class Main {
    public static void main(String[] args) {
        int [] x = {1,2,3,4,5};
        int sum = 0;
        for(int i = 0; i< x.length;i++) {
            sum += x[i];
        System.out.println(sum);
        }
    }
}
